class Calculator {
    // MARK: - Methods
    func add(_ x: Int, _ y: Int) -> Int {
        return x + y
    }
    
    func sub(_ x: Int, _ y: Int) -> Int {
        return x - y
    }
    
    func multi(_ x: Int, _ y: Int) -> Int {
        return x * y
    }
    
    func divide(_ x: Double, _ y: Double) -> Double {
        return x / y
    }
}
